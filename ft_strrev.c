#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int j;
	int k;

	j = 0;
	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i = i - (1);
	while (i > j)
	{
		k = str[i];
		str[i] = str[j];
		str[j] = k;
		i--;
		j++;
	}
	return (str);
}
